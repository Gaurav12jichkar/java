class pattren{
    /**
     * @param args
     */
    public static void main(String[] args) {
        int i,j;
        int num=6;
        for(i=1;i<=num;i++)
        {
            for(j=i-1;j<num;i++)
            {
                System.out.print("*");
            }
            System.out.print("\n");
        }

    }
}